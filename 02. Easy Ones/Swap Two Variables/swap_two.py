# Initialize variables with specific values
a = 6  # Assign 6 to variable a
b = 7  # Assign 7 to variable b

# Swapping variables using tuple unpacking
# Here, the values of a and b are swapped in one line.
a, b = b, a

# Print the swapped values to the console
print("a =", a)  # After swapping, a now holds the value that was originally in b (7)
print("b =", b)  # After swapping, b now holds the value that was originally in a (6)
