# Prompt the user to enter the first number and convert it to an integer
num1 = int(input("First number: "))

# Prompt the user to enter the second number and convert it to an integer
num2 = int(input("Second number: "))

# Determine the larger of the two numbers using the max() function
largest = max(num1, num2)

# Print the largest number entered by the user
print("Largest number you entered is: ", largest)
